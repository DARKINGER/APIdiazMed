
function numeros(index){
    switch(index){
        case 0:
            return 'numero 0';
            break;
        case 1:
            return 'numero 1';
            break;
        case 2:
            return 'numero 2';
            break;
        case 3:
            return 'numero 3';
            break;
        case 4:
            return 'numero 4';
            break;
        default:
            return 5;
            break;
    }
}
module.exports.numeros=numeros;

