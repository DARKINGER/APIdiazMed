
/**
 * constante que contiene un arreglo de frases.
 */
let frases = [
"frase 1",
"frase 2",
"frase 3"
]


/**
 * Retorna una frase dependiendo de cual sea el indice enviado.
 * 
 * @param {int} indice 
 * @returns string
 */
export function obtienefrase(indice){
    return frases[indice]; 
}

